#include <stdio.h>

int main(void){
	char x = 0;
	x = x | 4;    // 00000000 | 00000100 = 00000100
	x = x | 8;    // 00000100 | 00001000 = 00001000
	x = x | 128;  // 00001100 | 10000000 = 10001100

	x = x & ~4;   // 10001100 & 11111011 = 10001000
	x = x >> x;   // 10001000 >> 01000100

	return(0);
}
